#!/usr/bin/env bash
set -euo pipefail

usage() {
  cat >&2 <<EOF
Usage: $(basename "$0") <worktree-path> <branch>

Creates a git worktree at <worktree-path> on <branch> and generates isolated
.env.local files in site/ and siade/ so each worktree targets its own Postgres
databases.
EOF
  exit 1
}

[ $# -ge 2 ] || usage

WORKTREE_PATH="$1"
BRANCH="$2"

REPO_ROOT="$(git rev-parse --show-toplevel)"

if [ -e "$WORKTREE_PATH" ]; then
  echo "Error: $WORKTREE_PATH already exists" >&2
  exit 1
fi

if git -C "$REPO_ROOT" show-ref --verify --quiet "refs/heads/$BRANCH" \
   || git -C "$REPO_ROOT" for-each-ref --format='%(refname)' "refs/remotes/*/$BRANCH" | grep -q .; then
  git -C "$REPO_ROOT" worktree add "$WORKTREE_PATH" "$BRANCH"
else
  echo "Branch '$BRANCH' not found locally or on any remote — creating from develop"
  git -C "$REPO_ROOT" worktree add -b "$BRANCH" "$WORKTREE_PATH" develop
fi

WORKTREE_ABS="$(cd "$WORKTREE_PATH" && pwd)"
SLUG="$(basename "$WORKTREE_ABS" \
  | tr '[:upper:]' '[:lower:]' \
  | tr -c 'a-z0-9' '_' \
  | sed 's/__*/_/g; s/^_//; s/_$//')"

if [ -z "$SLUG" ]; then
  echo "Error: could not derive a slug from worktree basename" >&2
  exit 1
fi

write_env() {
  local app="$1"
  local db_prefix="$2"
  local target="$WORKTREE_ABS/$app/.env.local"

  if [ ! -d "$WORKTREE_ABS/$app" ]; then
    echo "Warning: $app/ not found in worktree, skipping .env.local" >&2
    return
  fi

  if [ -e "$target" ]; then
    echo "Warning: $target already exists, skipping" >&2
    return
  fi

  cat > "$target" <<EOF
# Generated by bin/setup_worktree.sh for worktree "$SLUG"
DATABASE_NAME_DEVELOPMENT=${db_prefix}_development_${SLUG}
DATABASE_NAME_TEST=${db_prefix}_test_${SLUG}
EOF
  echo "Wrote $target"
}

write_env site  admin_apientreprise
write_env siade siade

setup_app() {
  local app="$1"
  local dir="$WORKTREE_ABS/$app"

  [ -d "$dir" ] || return 0

  echo
  echo "==> Bootstrapping $app"
  (cd "$dir" && bundle install && bin/rails db:setup)
}

setup_app site
setup_app siade

echo
echo "Worktree ready at: $WORKTREE_ABS"
echo "DB slug:           $SLUG"
